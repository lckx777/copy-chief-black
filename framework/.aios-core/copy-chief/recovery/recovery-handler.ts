/**
 * Recover a failed handoff task using the 5-strategy escalation ladder.
 * Called by the handoff-engine when a sub-task fails.
 *
 * Flow:
 *   handoff failTask → recoverHandoffTask → executeRecovery
 *   → if resolved: handoff skipTask or retry
 *   → if not: pipeline pauses
 */
export function recoverHandoffTask(params: {
  offer_path: string;
  deliverable: string;
  task_id: string;
  task_type: string;
  error: string;
  phase: string;
  attempt_history?: AttemptRecord[];
}): RecoveryResult & { recommendation: 'retry' | 'skip' | 'pause' } {
  const ctx: RecoveryContext = {
    error: params.error,
    tool: params.task_type,
    offer_path: params.offer_path,
    phase: params.phase,
    deliverable: params.deliverable,
    attempt_history: params.attempt_history || [],
  };

  const result = executeRecovery(ctx);

  // Map recovery result to handoff recommendation
  let recommendation: 'retry' | 'skip' | 'pause';
  if (result.strategy_used === 'RETRY' && !result.resolved) {
    recommendation = 'retry';
  } else if (result.resolved && result.should_continue_pipeline) {
    recommendation = 'skip';
  } else {
    recommendation = 'pause';
  }

  // Log to handoff events for visibility
  try {
    const logDir = join(HOME, '.claude/logs');
    if (!existsSync(logDir)) mkdirSync(logDir, { recursive: true });
    const event = {
      type: 'recovery_attempted',
      deliverable: params.deliverable,
      task_id: params.task_id,
      offer_path: params.offer_path,
      strategy: result.strategy_used,
      resolved: result.resolved,
      recommendation,
      timestamp: new Date().toISOString(),
    };
    appendFileSync(join(logDir, 'handoff-events.jsonl'), JSON.stringify(event) + '\n');
  } catch { /* best effort */ }

  return { ...result, recommendation };
}

/**
 * Quick check: is an error likely recoverable without human intervention?
 * Used by pipeline-executor to decide whether to auto-retry.
 */
export function isAutoRecoverable(error: string): boolean {
  const errorClass = classifyError(error);
  return errorClass === 'TRANSIENT';
}
